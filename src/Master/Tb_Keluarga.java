    public void dataTable(){
        Object[] Baris = {"NO","NIP Pegawai","Nama Ayah","Pekerjaan Ayah","Nama Ibu","Pekerjaan Ibu","Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        tbData.setModel(tabmode);
        String sql ="select * from tb_keluarga order by nip_pegawai asc";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String nip_pegawai = hasil.getString("nip_pegawai");
                String nama_ayah = hasil.getString("nama_ayah");
                String pekerjaan_ayah = hasil.getString("pekerjaan_ayah");
                String nama_ibu = hasil.getString("nama_ibu");
                String pekerjaan_ibu = hasil.getString("pekerjaan_ibu");
                String alamat = hasil.getString("alamat");
                String [] data = {"",nip_pegawai,nama_ayah,pekerjaan_ayah,nama_ibu,pekerjaan_ibu,alamat};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data tidak bisa ditampilkan");
        }
    }
