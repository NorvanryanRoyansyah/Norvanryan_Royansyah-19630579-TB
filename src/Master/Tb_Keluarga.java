    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
      if (txtNip.getText().equals("")){
            JOptionPane.showMessageDialog(null, "NIP Pegawai Tidak Boleh Kosong!");
        } else {
            String sql = "insert into tb_keluarga (nip_pegawai,nama_ayah,pekerjaan_ayah,nama_ibu,pekerjaan_ibu,alamat) values (?,?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txtNip.getText());
                stat.setString(2, txtAyah.getText());
                stat.setString(3, txtKerjaAyah.getText());
                stat.setString(4, txtIbu.getText());
                stat.setString(5, txtKerjaIbu.getText());
                stat.setString(6, txtAlamat.getText());
                stat.executeUpdate();
                dataTable();
                resett();
                txtNip.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal Disimpan"+e);
            }
    }                                     
    }//GEN-LAST:event_btnSimpanActionPerformed

